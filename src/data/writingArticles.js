export const writingArticleMap = Object.fromEntries(writingArticles.map((article) => [article.slug, article]))

export function articleMatchesTopic(article, topic) {
  if (topic === 'all') return true

  const group = topicGroupMap[topic]
  if (!group?.categories) return article.category === topic
  return group.categories.includes(article.category)
}

export const writingTopicOptions = Object.entries(topicGroupMap)
  .map(([value, config]) => {
    const count =
      value === 'all'
        ? writingArticles.length
        : writingArticles.filter((article) => articleMatchesTopic(article, value)).length

    return {
      value,
      label: config.label,
      count,
    }
  })
  .filter((topic) => topic.count > 0)
