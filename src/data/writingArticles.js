function parseMdx(raw, slug) {
  const match = raw.match(/^---\n([\s\S]*?)\n---\n([\s\S]*)$/)
  const frontmatter = match?.[1] ?? ''
  const bodySource = match?.[2] ?? raw

  const data = {}
  let currentKey = null

  for (const line of frontmatter.split('\n')) {
    if (/^\s+-\s+/.test(line)) {
      if (!Array.isArray(data[currentKey])) data[currentKey] = []
      data[currentKey].push(line.replace(/^\s+-\s+/, '').trim())
      continue
    }

    const separatorIndex = line.indexOf(':')
    if (separatorIndex === -1) continue

    currentKey = line.slice(0, separatorIndex).trim()
    data[currentKey] = line.slice(separatorIndex + 1).trim()
  }

  const content = bodySource
    .replace(/^import .*$/gm, '')
    .replace(/^<ArticleInfographic.*$/gm, '')
    .replace(/^\s*\n/gm, '')
    .trim()

  return {
    slug,
    href: `/writing/${slug}`,
    title: data.title,
    excerpt: data.excerpt,
    publishDate: data.publishDate,
    date: data.publishDate.replaceAll('-', '.'),
    category: data.category,
    categoryLabel: categoryLabelMap[data.category] ?? data.category,
    readTime: data.readTime,
    meta: `${data.readTime} · ${categoryLabelMap[data.category] ?? data.category}`,
    tags: Array.isArray(data.tags) ? data.tags : [],
    content,
  }
}

